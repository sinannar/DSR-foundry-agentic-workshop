import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, '..')
const labsRoot = path.join(repoRoot, 'labs')
const docsRoot = path.join(repoRoot, 'docs')
const generatedRoot = path.join(docsRoot, '.generated', 'lab-steps')
const labsSidebarPath = path.join(docsRoot, '.vitepress', 'labs-sidebar.ts')

const STEP_DIR_PATTERN = /^\d{2}-/
const TRACK_ORDER = ['introduction-foundry-agent-service']
const TRACK_TITLES = {
  'introduction-foundry-agent-service': 'Introduction to Foundry Agent Service',
}

async function directoryExists(targetPath) {
  try {
    const stat = await fs.stat(targetPath)
    return stat.isDirectory()
  } catch {
    return false
  }
}

function titleFromReadme(markdown, fallback) {
  const match = markdown.match(/^#\s+(.+)$/m)
  return match?.[1]?.trim() ?? fallback
}

function cleanStepTitle(rawTitle) {
  return rawTitle.replace(/^(?:Lab|Step)\s*\d{2}\s*:?\s*/i, (prefix) => {
    return prefix.includes(':') ? prefix.replace(':', '') : prefix
  })
}

function trackTitle(trackName) {
  if (TRACK_TITLES[trackName]) {
    return TRACK_TITLES[trackName]
  }

  return trackName
    .split('-')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ')
}

async function writeLabsSidebarFile(sidebarItems) {
  const fileContent = [
    '// This file is generated by scripts/generate-vitepress-labs.mjs. Do not edit directly.',
    "import type { DefaultTheme } from 'vitepress'",
    '',
    'export const labsSidebarItems: DefaultTheme.SidebarItem[] = [',
    ...sidebarItems.map((item) => `  ${JSON.stringify(item)},`),
    ']',
    '',
  ].join('\n')

  await fs.writeFile(labsSidebarPath, fileContent, 'utf8')
}

async function writeGeneratedStep(trackName, stepName, sourceReadmePath, sourceMarkdown) {
  const outputDir = path.join(generatedRoot, trackName)
  const outputPath = path.join(outputDir, `${stepName}.md`)

  await fs.mkdir(outputDir, { recursive: true })

  const sourcePathForDisplay = path
    .relative(repoRoot, sourceReadmePath)
    .split(path.sep)
    .join('/')

  const generatedContent = [
    '<!-- This file is generated by scripts/generate-vitepress-labs.mjs. Do not edit directly. -->',
    '',
    `> Source: ${sourcePathForDisplay}`,
    '',
    sourceMarkdown.trimEnd(),
    '',
  ].join('\n')

  await fs.writeFile(outputPath, generatedContent, 'utf8')
}

async function main() {
  if (!(await directoryExists(labsRoot))) {
    throw new Error(`Labs directory not found: ${labsRoot}`)
  }

  await fs.rm(generatedRoot, { recursive: true, force: true })

  const trackEntries = await fs.readdir(labsRoot, { withFileTypes: true })
  const sidebarItems = []

  const sortedTrackEntries = trackEntries
    .filter((entry) => entry.isDirectory())
    .sort((a, b) => {
      const aOrder = TRACK_ORDER.indexOf(a.name)
      const bOrder = TRACK_ORDER.indexOf(b.name)

      if (aOrder !== -1 || bOrder !== -1) {
        if (aOrder === -1) return 1
        if (bOrder === -1) return -1
        return aOrder - bOrder
      }

      return a.name.localeCompare(b.name)
    })

  for (const trackEntry of sortedTrackEntries) {
    const trackName = trackEntry.name
    const trackPath = path.join(labsRoot, trackName)
    const stepEntries = await fs.readdir(trackPath, { withFileTypes: true })

    const trackSidebarItem = {
      text: trackTitle(trackName),
      link: `/labs/${trackName}`,
      collapsed: true,
      items: [],
    }

    const sortedStepNames = stepEntries
      .filter((entry) => entry.isDirectory() && STEP_DIR_PATTERN.test(entry.name))
      .map((entry) => entry.name)
      .sort((a, b) => a.localeCompare(b))

    for (const stepName of sortedStepNames) {
      const stepReadmePath = path.join(trackPath, stepName, 'README.md')
      const sourceMarkdown = await fs.readFile(stepReadmePath, 'utf8')
      const fallbackTitle = `Lab ${stepName}`
      const rawTitle = titleFromReadme(sourceMarkdown, fallbackTitle)
      const stepTitle = cleanStepTitle(rawTitle)
      const normalizedMarkdown = sourceMarkdown.replace(
        /^#\s+.+$/m,
        `# ${rawTitle}`,
      )

      await writeGeneratedStep(trackName, stepName, stepReadmePath, normalizedMarkdown)

      trackSidebarItem.items.push({
        text: stepTitle,
        link: `/.generated/lab-steps/${trackName}/${stepName}`,
      })
    }

    sidebarItems.push(trackSidebarItem)
  }

  await writeLabsSidebarFile(sidebarItems)

  console.log('Generated VitePress lab step pages and sidebar metadata')
}

await main()
